    /**
     * Returns the URL rewrite base slug for pSEO posts.
     *
     * Defaults to 'job'; overridable via the 'rzpa_seo_rewrite_base' option.
     *
     * @return string
     */
    public static function get_rewrite_base() : string {
        return (string) get_option( 'rzpa_seo_rewrite_base', 'job' );
    }

    /**
     * Flushes rewrite rules once if the transient flag is set.
     *
     * Call update_option('rzpa_seo_flush_rewrite', 1) to schedule a flush.
     *
     * @return void
     */
    public static function flush_rewrite_if_needed() : void {
        if ( get_option( 'rzpa_seo_flush_rewrite' ) ) {
            flush_rewrite_rules();
            delete_option( 'rzpa_seo_flush_rewrite' );
        }
    }

    // ── SEO Meta Output ───────────────────────────────────────────────────────

    /**
     * Outputs SEO meta tags in <head> for singular rzpa_pseo posts.
     *
     * Skipped when Yoast SEO or RankMath is active (they handle meta output).
     *
     * @return void
     */
    public static function output_seo_meta() : void {
        if ( ! is_singular( 'rzpa_pseo' ) ) {
            return;
        }

        // Defer to Yoast or RankMath if present.
        if ( defined( 'WPSEO_VERSION' ) || class_exists( 'RankMath' ) ) {
            return;
        }

        $post_id     = get_the_ID();
        $meta        = RZPA_SEO_Meta::get_meta( (int) $post_id );
        $title       = $meta['meta_title']       ?? '';
        $description = $meta['meta_description'] ?? '';
        $canonical   = $meta['canonical_url']    ?? '';
        $noindex     = ! empty( $meta['noindex'] );

        if ( $title ) {
            // Override document title via filter as well (handled by RZPA_SEO_Meta),
            // but also output a raw <title> as fallback inside wp_head.
            echo '<title>' . esc_html( $title ) . '</title>' . "\n";
        }

        if ( $description ) {
            echo '<meta name="description" content="' . esc_attr( $description ) . '">' . "\n";
        }

        if ( $canonical ) {
            echo '<link rel="canonical" href="' . esc_url( $canonical ) . '">' . "\n";
        } elseif ( $post_id ) {
            echo '<link rel="canonical" href="' . esc_url( get_permalink( $post_id ) ) . '">' . "\n";
        }

        if ( $noindex ) {
            echo '<meta name="robots" content="noindex,nofollow">' . "\n";
        }
    }

    // ── Loop Exclusions ───────────────────────────────────────────────────────

    /**
     * Excludes rzpa_pseo from the main query on home, feed, and archive pages.
     *
     * @param WP_Query $query
     * @return void
     */
    public static function exclude_from_loops( WP_Query $query ) : void {
        if ( is_admin() ) {
            return;
        }

        if ( ! $query->is_main_query() ) {
            return;
        }

        if ( ! ( $query->is_home() || $query->is_feed() || $query->is_archive() ) ) {
            return;
        }

        $post_type = $query->get( 'post_type' );

        // If query is not restricted to a specific type, build an exclusion list.
        if ( empty( $post_type ) || $post_type === 'any' || $post_type === 'post' ) {
            // For post_type='post', do nothing — rzpa_pseo is a separate type.
            // For empty / 'any', explicitly set post_types excluding rzpa_pseo.
            if ( empty( $post_type ) || $post_type === 'any' ) {
                $public_types = get_post_types( [ 'public' => true ] );
                unset( $public_types['rzpa_pseo'] );
                $query->set( 'post_type', array_values( $public_types ) );
            }
        }
    }

    /**
     * Secondary filter on the_posts to ensure rzpa_pseo pages are not shown
     * in standard loops that bypass pre_get_posts.
     *
     * @param WP_Post[] $posts
     * @return WP_Post[]
     */
    public static function maybe_filter_pseo_from_loop( array $posts ) : array {
        if ( is_admin() || is_singular() ) {
            return $posts;
        }

        global $wp_query;

        // Only filter on home/feed/archive main query.
        if ( ! isset( $wp_query ) || ! $wp_query->is_main_query() ) {
            return $posts;
        }

        if ( ! ( $wp_query->is_home() || $wp_query->is_feed() || $wp_query->is_archive() ) ) {
            return $posts;
        }

        return array_values(
            array_filter( $posts, static fn( WP_Post $p ) => 'rzpa_pseo' !== $p->post_type )
        );
    }

    // ── Helpers ───────────────────────────────────────────────────────────────

    /**
     * Returns post count object for the rzpa_pseo CPT.
     *
     * @return object  stdClass with publish/draft/etc. properties.
     */
    public static function get_pseo_post_count() : object {
        return wp_count_posts( 'rzpa_pseo' );
    }

    /**
     * Returns the WP_Post linked to a dataset via linked_post_id.
     *
     * @param int $dataset_id
     * @return WP_Post|null
     */
    public static function get_post_by_dataset( int $dataset_id ) : ?WP_Post {
        $dataset = RZPA_SEO_DB::get_dataset( $dataset_id );
        if ( empty( $dataset['linked_post_id'] ) ) {
            return null;
        }

        $post = get_post( (int) $dataset['linked_post_id'] );
        return ( $post instanceof WP_Post ) ? $post : null;
    }
}
