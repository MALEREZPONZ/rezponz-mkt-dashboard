/**
 * Rezponz SEO Engine – Main bootstrap class.
 *
 * Registers the rzpa_pseo custom post type, manages rewrite rules,
 * outputs SEO meta tags, and controls loop visibility.
 *
 * @package Rezponz\SEOEngine
 * @since   1.0.0
 */
class RZPA_SEO_Engine {

    // ── Bootstrap ─────────────────────────────────────────────────────────────

    /**
     * Registers all WordPress hooks.
     *
     * @return void
     */
    public static function init() : void {
        add_action( 'init',              [ __CLASS__, 'register_cpt' ] );
        add_action( 'after_setup_theme', [ __CLASS__, 'flush_rewrite_if_needed' ] );
        add_action( 'wp_head',           [ __CLASS__, 'output_seo_meta' ], 1 );
        add_action( 'pre_get_posts',     [ __CLASS__, 'exclude_from_loops' ] );
        add_filter( 'the_posts',         [ __CLASS__, 'maybe_filter_pseo_from_loop' ] );
    }

    // ── Custom Post Type ──────────────────────────────────────────────────────

